#include <stdio.h>

int main() {
    int num1 = 10, num2 = 5;
    printf("Sum of the numbers are\n", num1 + num2);
    printf("Difference of numbers are: %d\n", num1 - num2);
    printf("Product of numbers are: %d\n", num1 * num2);
    printf("Quotient of numbers are: %.6f\n", (float)num1 / num2);
    return 0;
}

